#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int		i;
	int		d;

	i = 0;
	d = 0;
	while (s1[d])
		d++;
	while (s2[i])
	{
		s1[d] = s2[i];
		i++;
		d++;
	}
	s1[d] = 0;
	return (s1);
}
